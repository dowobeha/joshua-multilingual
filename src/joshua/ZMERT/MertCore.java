      for (int it = firstIt; it <= iteration; ++it) {
        println("newCandidatesAdded[it=" + it + "] = " + newCandidatesAdded[it] + " (about " + newCandidatesAdded[it]/numSentences + " per sentence)",1);
      }

      println("",1);


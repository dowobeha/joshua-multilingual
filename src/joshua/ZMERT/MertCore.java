  public static void main(String[] args) throws Exception
  {
    // if bad args[], System.exit(80)

    String configFileName = args[0];
    String stateFileName = args[1];
    int currIteration = Integer.parseInt(args[2]);


    int randsToSkip = 0;
    int earlyStop = 0;
    double FINAL_score = 0.0;
    int[] maxIndex = null;

    if (currIteration == 1) {
      EvaluationMetric.set_knownMetrics();
      processArgsArray(cfgFileToArgsArray(configFileName),true);

      randsToSkip = 0;
      initialize(randsToSkip);

      println("----------------------------------------------------",1);
      println("Z-MERT run started @ " + (new Date()),1);
//      printMemoryUsage();
      println("----------------------------------------------------",1);
      println("",1);

      if (randInit) {
        println("Initializing lambda[] randomly.",1);

        // initialize optimizable parameters randomly (sampling uniformly from that parameter's random value range)
        lambda = randomLambda();
      }

      println("Initial lambda[]: " + lambdaToString(lambda),1);
      println("",1);

      FINAL_score = evalMetric.worstPossibleScore();
      maxIndex = new int[numSentences];
      for (int i = 0; i < numSentences; ++i) { maxIndex[i] = sizeOfNBest - 1; }
      earlyStop = 0;
    } else {

      EvaluationMetric.set_knownMetrics();
      processArgsArray(cfgFileToArgsArray(configFileName),false);

      ObjectInputStream in = new ObjectInputStream(new FileInputStream(stateFileName));
      double[] serA = (double[]) in.readObject();
      in.close();
      // contents of serA[]: last iteration, number of random numbers generated already, earlyStop, FINAL_lambda, lambda[], maxIndex[]
      // => length should be 4+numParams+numSentences

      if (serA.length < 2) {
        println("State file contains an array of length " + serA.length + "; was expecting at least 2");
        System.exit(81);
      }

      if ((int)serA[0] != currIteration-1) {
        println("Iteration in state file is " + (int)serA[0] + "; was expecting " + (currIteration-1));
        System.exit(82);
      }

      randsToSkip = (int)serA[1];
      initialize(randsToSkip); // declares lambda[], sets numParams and numSentences

      if (serA.length != 4+numParams+numSentences) {
        println("State file contains an array of length " + serA.length + "; was expecting " + (4+numParams+numSentences));
        System.exit(83);
      }

      earlyStop = (int)serA[2];
      FINAL_score = serA[3];

      for (int c = 1; c <= numParams; ++c) { lambda[c] = serA[3+c]; }

      maxIndex = new int[numSentences];
      for (int i = 0; i < numSentences; ++i) { maxIndex[i] = (int)serA[3+numParams+1+i]; }
    }


    double[] A = run_single_iteration(currIteration, minMERTIterations, maxMERTIterations, prevMERTIterations, earlyStop, maxIndex);

    if (A != null) {
      FINAL_score = A[0];
      earlyStop = (int)A[1];
      randsToSkip = generatedRands;
    }


    if (A != null && A[2] != 1) {

      double[] serA = new double[4+numParams+numSentences];
      serA[0] = currIteration;
      serA[1] = randsToSkip;
      serA[2] = earlyStop;
      serA[3] = FINAL_score;
      for (int c = 1; c <= numParams; ++c) { serA[3+c] = lambda[c]; }
      for (int i = 0; i < numSentences; ++i) { serA[3+numParams+1+i] = maxIndex[i]; }

      ObjectOutputStream out = new ObjectOutputStream(new FileOutputStream(stateFileName));
      out.writeObject(serA);
      out.flush();
      out.close();

      System.exit(91);

    } else {
      // done

      println("",1);

      println("----------------------------------------------------",1);
      println("Z-MERT run ended @ " + (new Date()),1);
//      printMemoryUsage();
      println("----------------------------------------------------",1);
      println("",1);
      println("FINAL lambda: " + lambdaToString(lambda) + " (" + metricName + ": " + FINAL_score + ")",1);
      // check if a lambda is outside its threshold range
      for (int c = 1; c <= numParams; ++c) {
        if (lambda[c] < minThValue[c] || lambda[c] > maxThValue[c]) {
          println("Warning: after normalization, lambda[" + c + "]=" + f4.format(lambda[c]) + " is outside its critical value range.",1);
        }
      }
      println("",1);

      // delete intermediate .temp.*.it* decoder output files
      for (int iteration = 1; iteration <= maxMERTIterations; ++iteration) {
        deleteFile(decoderOutFileName+".temp.sents.it"+iteration);
        deleteFile(decoderOutFileName+".temp.feats.it"+iteration);
        deleteFile(decoderOutFileName+".temp.stats.it"+iteration);
      }

      // delete .temp.stats.merged file
      deleteFile(decoderOutFileName+".temp.stats.merged");


      finish();

      deleteFile(stateFileName);
      System.exit(90);
    }

  }


